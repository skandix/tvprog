# Show Search
## URL: /search/shows?q=:query
## Example: http://api.tvmaze.com/search/shows?q=girls

# Show single search
## URL: /singlesearch/shows?q=:query
## Example: http://api.tvmaze.com/singlesearch/shows?q=girls
## Example: http://api.tvmaze.com/singlesearch/shows?q=girls&embed=episodes

# Show Lookup
## URL: /lookup/shows?tvrage=:id or /lookup/shows?thetvdb=:id
## Example: http://api.tvmaze.com/lookup/shows?tvrage=24493
## Example: http://api.tvmaze.com/lookup/shows?thetvdb=81189
## Example: http://api.tvmaze.com/lookup/shows?imdb=tt0944947

# People search
## URL: /search/people?q=:query
## Example: http://api.tvmaze.com/search/people?q=lauren
